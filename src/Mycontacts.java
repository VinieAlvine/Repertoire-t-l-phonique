    public void populateJtable() {
        
        contactQuery cq = new contactQuery();
        ArrayList<Contact> ctList = cq.contactList(currentUserId);
        // `tel1`, `tel2`, `email`, `mention`, `pic`
        String[] colNames = {"Id","Nom","Numero 1","Numero2","Email","Mention","Image"};
        Object[][] rows = new Object[ctList.size()][7];
        
        for(int i = 0;i < ctList.size();i++){
            rows[i][0] = ctList.get(i).getCid();
            rows[i][1] = ctList.get(i).getnom();
            rows[i][2] = ctList.get(i).gettel1();
            rows[i][3] = ctList.get(i).gettel2();
            rows[i][4] = ctList.get(i).getemail();
            rows[i][5] = ctList.get(i).getmention();
            
            ImageIcon pic = new ImageIcon(new ImageIcon
                                         (ctList.get(i).getpic())
                                         .getImage()
                                         .getScaledInstance(120,80,Image.SCALE_SMOOTH));
            rows[i][6] = pic;
            
        }
        
        MyModel mmd = new MyModel(rows,colNames);
        jTable1.setModel(mmd);
        jTable1.setRowHeight(80);
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(120);
        
    }
    
    
    
