    private void jButtonAjoutercontactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjoutercontactActionPerformed
      
      
      String nom = jTextFieldnom.getText();
      String tel1 = jTextFieldtel1.getText();
      String tel2 = jTextFieldtel2.getText();
      String email = jTextFieldemail.getText();
      String mention = jComboBoxmention.getSelectedItem().toString();
      // vous pouvez vérifier si tous les champs sont vides
      // mais dans ce cas je vérifierai seulement si l’utilisateur sélectionne une image
      // et pour ce faire, nous allons utiliser imagepath 
      
      byte[] img = null;
      
      if(imagePth != null){
      try{
          Path pth = Paths.get(imagePth);
          img = Files.readAllBytes(pth); 
          Contact c = new Contact(null,nom,tel1,tel2,email,mention,img,currentUserId);
          contactQuery cq = new contactQuery();
          cq.insertContact(c);
          refreshJtable();
          clearFields();
      } catch(IOException ex) {
          Logger.getLogger(Mycontacts.class.getName()).log(Level.SEVERE,null,ex);
          }  
      }  else{
          
          JOptionPane.showMessageDialog(null,"vous devez selection une photo de profile");
      }
      
      
        
    
    }//GEN-LAST:event_jButtonAjoutercontactActionPerformed
